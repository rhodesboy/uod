				<div class="pull">
		 	 		<h2 class="under">1. Choose your Course</h2>
					<p>We have over 200 full-time courses for Undergraduate Study.  
					Choose a course using our <a href="http://www.dundee.ac.uk/study/choose-your-course/"> Course Search</a> 
					then apply through UCAS.  Still can't decide? Join our <a href="http://www.dundee.ac.uk">Visit/Open Day</a>.</p>
				</div>

				<h2 class="under">2. Register with UCAS</h2>
				<p>Register with the <a href="http://www.ucas.com/apply">UCAS Apply online</a> 
				tracking application.  The <a href="http://www.ucas.tv/ucas/video/69dr7">Introduction to Apply</a> 
				video guide shows you how to fill in your application.</p>
				
				<p>Additional information about applying to specific courses, Part-time, Distance Learning and Study Abroad 
				courses can be found on<a href="http://www.dundee.ac.uk/undergraduate/courses/"> individual course webpages</a>.</p>

				<h3>Key Dates</h3>
				<p>Apply a year before you wish to start university:</p>

				<table class="table table-striped table-md">
				<tr>
					<th>Applications begin</th>
					<td>1 Sept</td>
				</tr>
				<tr>
					<th>Applications closing date</th>
					<td>15 Jan</td>
				</tr>
				<tr>
					<th>Medicine or Dentistry closing date</th>
					<td>15 Oct</td>
				</tr>
				</table>

				<p>Late applications received at UCAS between 16 January and 30 June will be 
				considered at the discretion of the university.</p>
				
				<p>If you plan to take a year out, apply  two years in advance of starting university.</p>

				<p><a href="http://www.ucas.com/apply/key-dates">UCAS Key Dates for Undergraduates</a></p>

				<h2 class="under">3. Your application</h2>
				<p>UCAS will send your online application to our Admissions office. A minimum of two staff members go  
				through the information you have provided to get an understanding of  your motivation for choosing 
				your course at the University of Dundee.</p>

				<h2 class="under">4. Interviews, Portfolios and Selection Tests</h2>

				<p>Read our<a href="http://www.dundee.ac.uk/undergraduate/courses/"> individual course webpages</a> 
				to find out if your chosen course requires an interview, portfolio or selection test.</p>

				<p>We will provide guidance of what is expected of you once we have made our initial assessment  
				after the UCAS  deadline.</p>

				<p>There are no set dates or times for interviews or tests, but  generally they take place between  
				January and April. If there are  dates when you are unavailable for interview (e.g. due to examinations) 
				you can mark these in the appropriate space on your UCAS application.</p>

				<h2 class="under">5. Our Offer</h2>
				<p>UCAS allows our admissions tutors to make three types of offers of  admission:</p>

				<dl class="well effect2">
					<dt>Unconditional Offer</dt>
					<dd>If you have already obtained the necessary entry qualifications.</dd>	
					<dt>Conditional Offer</dt>
					<dd>if you still need to obtain  the passes/grades specified in the offer, 
						in examinations still to be taken, to secure entry. If you obtain these passes, 
						your offer  will change to become an unconditional offer, 
						meaning you definitely  have a place. To secure a place, all 
						conditions of an offer must be met by 31 August.  
						This date refers also to applicants  who have deferred  their entry.</dd>	
					<dt>Rejection</dt>
					<dd>if you are not suitably qualified.</dd>
				</dl>

				<p><a href="http://www.ucas.tv/ucas/video/LvWGy">UCAS have a useful video on how to Make sense of your offers</a></p>
				
				<p>We will make our decision after the <strong>1 February</strong>. 
				This is to  ensure we consider all applications equally that apply by the UCAS deadline.</p><p>
				If you apply for Level 2 or Level 3 entry  to a course but you do not 
				meet the conditions of the offer, you will <strong>not</strong> be automatically 
				considered for entry to a lower Level of entry as there may no longer be vacancies.</p>

				<h2 class="under">6. Received an Offer?</h2>
				<p>
				Congratulations! Successful applicants (those receiving an offer of admission) have the 
				opportunity to join a <a href="http://www.dundee.ac.uk/undergraduate/visits/post_application_visits.htm">
				Post-Application Visit</a> before replying to their offer.</p>

				<div class="line">&nbsp;</div>

				<h2>No offers? Missed the deadline?</h2>

				<div class="row">
					<div class="col-md-6">
						<h3>UCAS Extra</h3>
						<p>You can choose up to 5 courses on your UCAS  Application.</p> 
						<p>If you have used up all of your original 5 choices, 
						but are holding no offers, <a href="http://www.ucas.com/how-it-all-works/
						undergraduate/tracking-your-application/adding-extra-choices">UCAS Extra</a> 
						will give you the opportunity to make further applications and receive  
						an offer before the exam results are published and Clearing starts. UCAS 
						will write to you if you are eligible for Extra.</p>
					</div>
					<div class="col-md-6">	
						<h3>Clearing</h3>
						<p>Our <a href="http://dundee.ac.uk/clearing/">clearing service</a> 
						operates from <strong>1 August until the end of September</strong>.  You can use Clearing if:</p>

						<ul>
							<li>you're not accepted on a course</li>	
							<li>you decline all offers</li>	
							<li>or you applied after 30 June</li>
						</ul>

						<p>If you still want to go to University the same year, 
						add a clearing choice on your UCAS application. The<a href="http://www.ucas.tv/ucas/video/vjQkm"> 
						Clearing (how to)</a> guide explains what you  need to do.</p>
					</div>
				</div><!-- / .row -->
				
